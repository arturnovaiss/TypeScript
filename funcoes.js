function multiplicar(a, b) {
    return a * b;
}
// Exemplo de uso:
var resultadoMultiplicacao = multiplicar(3, 4);
console.log(resultadoMultiplicacao); // Output: 12
function saudacao(nome) {
    return "Ol\u00E1 ".concat(nome);
}
// Exemplo de uso:
var mensagemSaudacao = saudacao("João");
console.log(mensagemSaudacao); // Output: Olá João
